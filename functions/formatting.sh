#!/bin/bash

# "Constants"
bold=$(tput bold)
normal=$(tput sgr0)

#==============================================================================
# bld - Bold text
#==============================================================================
function bld {
  echo "${bold}$1${normal}"
}

#==============================================================================
# slugify - Slugify a string
#==============================================================================
function slugify {
  echo $1 | tr '[:upper:]' '[:lower:]' | sed 's/ /-/g' | sed 's/[^a-z0-9-]//g'
}

#==============================================================================
# nclr - Clear n lines
#==============================================================================
nclr () { local j; for ((j = 0; j <= "${1:-1}"; j++ )); do tput cuu1; done; tput ed; }
